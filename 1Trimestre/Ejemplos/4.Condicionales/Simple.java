public class Simple {
    public static void main(String[] args){
        
        if(true){
            System.out.println("true: Esto se va a imprimir");
        }

        if(false){
            System.out.println("false: Esto NO se va a imprimir");
        }

        if(!true){
            System.out.println("!true: Esto NO se va a imprimir");
        }

        if(!false){
            System.out.println("!false: Esto se va a imprimir");
        }

    }
}